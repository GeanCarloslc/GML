package model;

/**
 *
 * @author topga
 */
public class RemoverProdutoModel {
    
    private int idDoProduto;
    private String categoria;
    private String nomeDoProduto;
    private float valor;
    private int quantidade;
    
    
    //CONSTRUTOR

    public RemoverProdutoModel() {
    }

    public RemoverProdutoModel(String categoria) {
        this.categoria = categoria;
    }
    
    
    
    
    
    //GETTERS AND SETTERS

    public int getIdDoProduto() {
        return idDoProduto;
    }

    public void setIdDoProduto(int idDoProduto) {
        this.idDoProduto = idDoProduto;
    }

    public String getCategoria() {
        return categoria;
    }

    public void setCategoria(String categoria) {
        this.categoria = categoria;
    }

    public String getNomeDoProduto() {
        return nomeDoProduto;
    }

    public void setNomeDoProduto(String nomeDoProduto) {
        this.nomeDoProduto = nomeDoProduto;
    }

    public float getValor() {
        return valor;
    }

    public void setValor(float valor) {
        this.valor = valor;
    }

    public int getQuantidade() {
        return quantidade;
    }

    public void setQuantidade(int quantidade) {
        this.quantidade = quantidade;
    }
    
    
    
}
